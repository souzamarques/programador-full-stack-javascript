/* Tipo number: utilizado para fazer uma representação numérica.
   Todos os numeros estão incluidos nesse tipo, seja inteiros ou decimais. */

console.log("Testando:", 5)
console.log("Testano novamente:", 2 * 3.5)

/* Tipo string: cadeia de caracteres */
console.log("Spaceship")
console.log('Spaceship em aspas simples')

/* Tipo booleano: assume apenas dois valores: true e false */
console.log("5 > 10:", 5 > 10)
console.log("100 > 10:", 100 > 10)

/* Tipo null e undefined:
   null: representa valor nulo ou vazio.
   undefined: interpretar que é um valor indefinido. Utilizado quando a linguagem
              não consegue definir um tipo. */